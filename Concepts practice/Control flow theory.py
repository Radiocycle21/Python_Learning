#  Control flow
# ------------------------------------------------------------------------------------------------------

# control flow is the logic you write to control how your code runs. 

# control flow is done using -- 
# 1. conditional statment
# 2. loops

# conditional statements-- if, else, elif 
# loops- for, while, (break, pass, continue -- control commands used in the loops)

# Boolean expressions- build questions(condtitions) python can evaluate as "yes" or "no" (True or false)

# different boolean expressions are -- 
# values                - True, False
# functions             - bool(), any(), all(), ininstance()
# comparison operators  - ==, !=, <, >, >=, <=
# logical operators     - and, or, not
# membership operators  - in, not in
# identity operators    - is, is not

